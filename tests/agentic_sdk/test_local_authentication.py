class TestLocalAuthenticationIntegration:
    """Integration tests for local authentication."""

    def test_all_five_methods_produce_local_mode(self, monkeypatch):
        """Verify all 5 authentication methods result in local mode."""
        
        # Method 1: Explicit
        client1 = Client(
            api_key=TEST_LOCAL_TOKEN,
            instance_url="http://localhost:4321",
            local=True,
        )
        assert client1.session.mode == "local"

        # Method 2: Auto-detect
        client2 = Client(
            api_key=TEST_LOCAL_TOKEN,
            instance_url="http://localhost:4321",
        )
        assert client2.session.mode == "local"

        # Method 3: Env vars (both)
        monkeypatch.setenv("WXO_USER_TOKEN", TEST_LOCAL_TOKEN)
        monkeypatch.setenv("WXO_AUTH_URL", "http://localhost:4321")
        client3 = Client()
        assert client3.session.mode == "local"

    @patch("ibm_watsonx_orchestrate_clients.common.service_instance.local_service_instance.LocalServiceInstance")
    def test_methods_4_and_5_with_mocked_token_generation(
        self, mock_local_instance, monkeypatch
    ):
        """Test methods 4 and 5 that require token generation."""
        mock_instance = MagicMock()
        mock_instance.tenant_access_token = TEST_LOCAL_TOKEN
        mock_local_instance.return_value = mock_instance

        # Method 4: Env URL only
        monkeypatch.setenv("WXO_AUTH_URL", "http://localhost:4321")
        monkeypatch.delenv("WXO_USER_TOKEN", raising=False)
        client4 = Client()
        assert client4.session.mode == "local"

        # Method 5: Zero-config
        monkeypatch.delenv("WXO_AUTH_URL", raising=False)
        client5 = Client()
        assert client5.session.mode == "local"
        assert client5.session.base_url == f"{DEFAULT_LOCAL_SERVICE_URL}/api/v1"


