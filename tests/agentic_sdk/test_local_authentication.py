class TestLocalAuthenticationExplicit:
    """Test explicit local mode with provided token."""

    def test_explicit_local_mode_with_token(self):
        """Test: Client(api_key=token, instance_url=url, local=True)"""
        client = Client(
            api_key=TEST_LOCAL_TOKEN,
            instance_url="http://localhost:4321",
            local=True,
        )

        assert client.session.mode == "local"
        assert client.session.base_url == "http://localhost:4321/api/v1"
        assert client.session.access_token == TEST_LOCAL_TOKEN
        assert client.session.authenticator is None

    def test_explicit_local_mode_with_bearer_prefix(self):
        """Test that Bearer prefix is automatically stripped."""
        client = Client(
            api_key=f"Bearer {TEST_LOCAL_TOKEN}",
            instance_url="http://localhost:4321",
            local=True,
        )

        assert client.session.access_token == TEST_LOCAL_TOKEN

    def test_explicit_local_mode_custom_port(self):
        """Test local mode with custom port."""
        client = Client(
            api_key=TEST_LOCAL_TOKEN,
            instance_url="http://localhost:8080",
            local=True,
        )

        assert client.session.base_url == "http://localhost:8080/api/v1"

    def test_explicit_local_mode_with_verify_false(self):
        """Test local mode with SSL verification disabled."""
        client = Client(
            api_key=TEST_LOCAL_TOKEN,
            instance_url="http://localhost:4321",
            local=True,
            verify=False,
        )

        assert client.session.verify is False


